const pricing = (total, kitPrice) => ({
  finalUnitPrice: total,
  pricePerKit: kitPrice,
  initialEstimatePrice: Math.round(total * 1.04),
  currency: 'DKK',
})

const stats = (checked, approved, rejected, totalComponents, totalKits) => ({
  checked,
  totalComponents,
  approved,
  required: rejected,
  rejected,
  totalKits,
})

const order = ({
  id,
  name,
  quoteId,
  status = UNIVERSITY_STATUS.ACTIVE_ORDER,
  checked = 0,
  approved = 0,
  rejected = 0,
  totalComponents = 6,
  totalKits,
  total,
  kitPrice,
  progressStep,
}) => ({
  id,
  name,
  quoteId,
  status,
  stats: stats(checked, approved, rejected, totalComponents, totalKits),
  pricing: pricing(total, kitPrice),
  progressStep,
})

